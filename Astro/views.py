from django.shortcuts import render
from django.http import HttpResponse, HttpResponseRedirect
from django.contrib.auth import authenticate, login, logout
from django.contrib.auth.decorators import login_required
from django.urls import reverse
from django.contrib import messages

from .forms import CreateUserForm, UserProfileInfoForm
from django.conf import settings


def index(request):
    username = None
    if request.user:
        username = request.user.username
        return render(request, 'Home.html', {'username': username.capitalize()})
    return render(request, 'Home.html')

@login_required
def book(request):
    return render(request, 'Book.html')

@login_required
def user_logout(request):
    logout(request)
    return HttpResponseRedirect(reverse('index'))

def register(request):
    registered = False
    if request.method == 'POST':
        user_form = CreateUserForm(data=request.POST)
        profile_form = UserProfileInfoForm(data=request.POST)
        if user_form.is_valid() and profile_form.is_valid():
            user = user_form.save()
            user.set_password(user.password)
            user.save()
            profile = profile_form.save(commit=False)
            profile.user = user
            if 'profile_pic' in request.FILES:
                print('found it')
                profile.profile_pic = request.FILES['profile_pic']
            profile.save()
            registered = True
        else:
            print(user_form.errors,profile_form.errors)
        return HttpResponseRedirect(reverse('Astro:user_login'))
    else:
        user_form = CreateUserForm()
        profile_form = UserProfileInfoForm()
    return render(request,'register.html',
                          {'user_form':user_form,
                           'profile_form':profile_form,
                           'registered':registered})

def user_login(request):
    if request.method == 'POST':
        username = request.POST.get('login_user')
        password = request.POST.get('login_pass')
        user = authenticate(request, username = username, password = password)
        print(user)
        if user:
            if user.is_active:
                login(request,user)
                return HttpResponseRedirect(reverse('index'))
            else:
                return HttpResponse("Your account was inactive.")
        else:
            print("Someone tried to login and failed.")
            print("They used username: {} and password: {}".format(username,password))
            messages.error(request, 'Either thy Username or Password is incorrect')
            return HttpResponseRedirect(reverse('Astro:user_login'))
    else:
        return render(request, 'login.html', {})
